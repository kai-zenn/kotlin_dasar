package basics.bolo

fun main(){
    // boolean expression
    val x = 10
    val y = 9
    println(x > y) // menghasilkan nilai true

    // boolean values
    // tipe boolean bisa deklarasikan dengan Boolean lalu harus diikuti dengan value TRUE atau FALSE
    val kotlinGacor: Boolean = true
    val nodeJsJelek: Boolean = false
    println(kotlinGacor)
    println(nodeJsJelek)
}