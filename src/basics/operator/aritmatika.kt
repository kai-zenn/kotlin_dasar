package basics.operator

fun main() {
    var nomor: Int = 100

    // addition (+)
    var tambah = nomor + 10

    // subtraction (-)
    var kurang = nomor - 10

    // multiplication (*)
    var perkalian = nomor * 5

    // division (/)
    var pembagian = nomor / 10

    // modulus (%), sisa pembagian
    var modulus = nomor % 9

    // increment (++), angka akan bertambah terus sesuai dengan kondisi yang diberikan nanti
    // var increment = ++nomor

    // decrement (--), kebalikan dari increment
    // var decrement = --nomor

    println(tambah)
    println(kurang)
    println(perkalian)
    println(pembagian)
    println(modulus)
}